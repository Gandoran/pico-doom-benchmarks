angle_bam LutAtan(n_fixed ratio) {
    int sign = 0;
    if (ratio < 0) {
        sign = 1;
        ratio = -ratio;
    }
    angle_bam result;
    if (ratio > 65536) {
        n_fixed inv_ratio = ((int64_t)65536 * 65536) / ratio;
        unsigned idx = ((unsigned)inv_ratio * 4095u) / 65536u;
        if (idx >= 4096u) idx = 4095u;
        result = 2048u - atan_table[idx];
    } else {
        unsigned idx = ((unsigned)ratio * 4095u) / 65536u;
        if (idx >= 4096u) idx = 4095u;
        result = atan_table[idx];
    }
    if (sign) {
        return (angle_bam)(-((int)result));
    }
    return result;
}

